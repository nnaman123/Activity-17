import math
print('The Floor and Ceiling value of 23.56 are: ' + str(math.ceil(23.56)) + ' and ' + str(math.floor(23.56)))
x = 10
y = -15
print('The value of x after copying the sign from y is: ' + str(math.copysign(x, y)))
print("The absolute value of -96 and 56 is: " + str(math.fabs(-96)) + ' and ' + str(math.fabs(56)))

print("The HCF of 24 and 56 is: " + str(math.gcd(24, 56)))
